// Initialize and add the map
function initMap(latitude, longitude) {
   // The location of store_location
   if (latitude == undefined) {
      // 페이지 열렸을 때 초기값 ,종각
      latitude = 37.5705583;
      longitude = 126.9816646;
   }
   var store_location = { lat: latitude, lng: longitude };
   // The map, centered at store_location
   var map = new google.maps.Map(document.getElementById("map"), {
      zoom: 15,
      center: store_location,
   });
   // The marker, positioned at store_location
   var marker = new google.maps.Marker({
      position: store_location,
      map: map,
   });
}
