const locationSelector = $("input");

const store_name = document.querySelector(".store_name"),
   about_store = document.querySelector(".about_store"),
   opening_hours = document.querySelector(".opening_hours"),
   address = document.querySelector(".address"),
   tel = document.querySelector(".tel");

locationSelector.click(function () {
   // console.log(this.value);
   const thisValue = localStorage.getItem(this.value);
   const obj = JSON.parse(thisValue);
   store_name.innerHTML = this.value + "<span>" + obj.store_name + "</span>";
   about_store.innerText = obj.about_store;
   opening_hours.innerText = obj.opening_hours;
   address.innerText = obj.address;
   tel.innerText = obj.tel;
   initMap(obj.lat, obj.lng);
});

////////////

locationSelector.click(function () {
   $(this).addClass("hover");
});

locationSelector.mouseover(function () {
   locationSelector.removeClass("hover");
   $(this).addClass("hover");
});
